                    <h2>カードの図柄</h2>
                    <p>正義のカードには、玉座に真っ直ぐ座る女性の姿が描かれています。彼女は赤いローブをまとい、冷静な表情で前を見つめています。右手には「剣」を掲げ、これは真実を見抜く力と決断の象徴です。剣が垂直に立っているのは、正義がまっすぐな意志と公正さによって導かれていることを意味しています。
                    </p>

                    <p>左手には「天秤」が握られており、これはバランス、公平、公正な判断を表します。天秤には偏りがなく、すべての判断が感情ではなく理性によって下されるべきであるという教えが込められています。背景には垂れ幕と柱が描かれており、これは法や制度の厳格さと秩序を示しています。
                    </p>

                    <p>また、正義の女性は王冠を被っており、その中央には小さな四角形の装飾があります。これは、明確で揺るぎない道徳的価値観を象徴しています。彼女の姿勢や視線、身にまとう衣装の整然とした様子からは、「感情に流されず、冷静に真実を見極める」姿勢が強く感じられます。
                    </p>

                    <h2>カードの意味</h2><br>
                    <h4>正位置の意味</h4>
                    <p>公平。バランス。判断力。誠実さ。因果応報。正しい選択。法と秩序。正義感。誠意。客観性。倫理観。正当な評価。信頼。責任を果たす。冷静な判断。誠実な行動。中立性。説明責任。事実に基づく判断。ルールを守る姿勢。
                    </p>

                    <h4>逆位置の意味</h4>
                    <p>不正。不公平。偏った判断。偽り。ごまかし。責任逃れ。偏見。真実から目を背ける。選択ミス。不誠実な対応。隠ぺい。自己中心的。被害者意識。説明不足。誤解。無責任。感情に流される。正当性を欠いた行動。
                    </p>

                    <h2>カードが告げていること</h2><br>
                    <h4>正位置の場合</h4>
                    <p>正義が逆位置で出たとき、それは「今の判断や行動が、公正さや誠実さを欠いている可能性がある」という警告です。自分の中に偏見や先入観が入り込んでいないか、また、感情的になって事実から目を背けていないかを振り返ってみましょう。
                    </p>

                    <p>また、このカードは「因果応報」の意味も持っています。過去に誠実な行動をしてきた人には、正当な評価や報酬が与えられるでしょう。一方で、隠し事やごまかしがあった場合は、それが明るみに出る可能性もあります。正義の剣は真実を切り開き、バランスを取り戻そうとします。今のあなたは、誠実さと責任感を持って行動することで、物事が良い方向へと進むでしょう。
                    </p>

                    <p>人間関係においても、公平な視点を持つことが大切です。一方的な意見や感情に流されるのではなく、相手の立場も尊重しながら、バランスを保つことが信頼を築く鍵になります。</p>


                    <h4>逆位置の場合</h4>
                    <p>正義が逆位置で出たとき、それは「今の判断や行動が、公正さや誠実さを欠いている可能性がある」という警告です。自分の中に偏見や先入観が入り込んでいないか、また、感情的になって事実から目を背けていないかを振り返ってみましょう。
                    </p>

                    <p>また、不正や不誠実な行為が露見することを示す場合もあります。たとえば、秘密にしていたことが表に出たり、責任を果たさなかったことで批判を受けるなど、過去の言動に対して結果が返ってくる時期かもしれません。逆位置の正義は、因果のバランスが崩れている状態を表します。
                    </p>

                    <p>あなた自身が誰かから不当な扱いを受けていると感じることもあるでしょう。そのような時こそ、冷静さを失わず、自分の立場をしっかりと主張し、正当な手段で解決を目指すことが重要です。感情的な反発や強引なやり方では、かえって状況を悪化させる恐れがあります。
                    </p>

                    <p>今は「正しさとは何か」を深く見つめ直すタイミングです。周囲の意見に振り回されず、自分の信じる価値観を大切にしながら、誠実に行動することが、長い目で見て必ず自分を守ってくれます。</p>




